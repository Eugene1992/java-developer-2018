package oopHomeWork1;

public class User {

    String firstname, lastname;
    int age, salary, orders, friends;

    User(String firstname, String lastname, int friends, int age, int salary, int orders) {
        this.firstname = firstname;
        this.lastname = lastname;
        this.friends = friends;
        this.age = age;
        this.salary = salary;
        this.orders = orders;

    }

}
